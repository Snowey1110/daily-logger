UI_STRINGS: Dict[str, Dict[str, str]] = {"en": _EN, "zh": _ZH}


def normalize_ui_language(raw: Optional[str]) -> str:
    s = (raw or "").strip().lower()
    if s in ("zh", "zh-cn", "cn", "chinese", "中文"):
        return "zh"
    return "en"


def ui_translate(lang: str, key: str, **kwargs: object) -> str:
    row = UI_STRINGS.get(lang) or UI_STRINGS["en"]
    template = row.get(key) or UI_STRINGS["en"].get(key, key)
    if kwargs:
        try:
            return template.format(**kwargs)
        except (KeyError, ValueError):
            return template
    return template
